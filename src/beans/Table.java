package beans;

/**
 *
 * @author Sarah
 */
public class Table {
    private String tableName;

    public Table(String tableName) {
        this.tableName = tableName;
    }

    public String getTableName() {
        return tableName;
    }

    public void setTableName(String tableName) {
        this.tableName = tableName;
    }
    
    @Override
    public String toString() {
        return tableName;
    }
    
    
}
